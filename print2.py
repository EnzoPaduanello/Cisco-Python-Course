print(2+2,"\n")#Sintaxe de operação


print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.,"\n")#Sintaxe de exponenciação


print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.,"\n")#Sintaxe de multiplicação


print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.,"\n")#Sintaxe de divisão


print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print(-6 // 4)
print(6. // -4,"\n")#Sintaxe de divisão inteira(floor division)

print(12 % 4.5)
print(14 % 4, "\n")#Essa operação só entregará a sobra da divisão


print(-4 + 4)
print(-4. + 8, "\n")#Sintaxe de soma


print(-1.1)
print(+2,"\n")


#Prioridade de operações 1	+, -	unário
#                        2	**	
#                        3	*, /, //, %	
#                        4	+, -	binário

print((5 * ((25 % 13) + 100) / (2 * 13)) // 2, "\n")